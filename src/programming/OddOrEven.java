package programming;
//simple program to check number is even or ODD
public class OddOrEven {
	
	public static void main(String[] args) {
		
		int number=33;
		
		if (number % 2 == 0) {
			System.out.println("This number is even");
		}else {
			System.out.println("This number is odd");
		}
		
	}

}
